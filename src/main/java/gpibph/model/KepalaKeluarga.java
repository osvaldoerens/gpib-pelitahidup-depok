package gpibph.model;

public class KepalaKeluarga {

	public int id;
	public String no_kk;
	public String nama_kepala_keluarga;	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNo_kk() {
		return no_kk;
	}
	public void setNo_kk(String no_kk) {
		this.no_kk = no_kk;
	}
	public String getNama_kepala_keluarga() {
		return nama_kepala_keluarga;
	}
	public void setNama_kepala_keluarga(String nama_kepala_keluarga) {
		this.nama_kepala_keluarga = nama_kepala_keluarga;
	}
	
}
